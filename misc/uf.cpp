    res = (1<<(p+1)) - 1;
    p++;
    if(res > N){
        cout<<p-1<<nl;
        return;
    }
    if(res == N){
        cout<<p<<nl;
        return;
    }
    N -= res;
    int q, rem;
    q = (int)N/k;
    rem = N%k;
    p += q;
    if(rem)
        p++;
